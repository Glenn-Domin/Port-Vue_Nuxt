            <p>Vanzer has an ambivalence toward the order today. On one hand they brought war to his world, and on the other he respected their tenets and admired them in the past, initially seeking to join them. Former members of the order may get a complicated reception. He seeks to learn more about the force, but may balk at their order's more recent history.</p>
          </li>
          <li>
            <h4>The Separatists</h4>
            <p>While Vanzer's perspective on the Jedi Order was nuanced, his views on the Separatists are far more straightforward. He hated the Separatists for bombarding his homeworld.</p>
          </li>
          <li>
            <h4>The Sith</h4>
            <p>Vanzer sees the Sith as morally bankrupt, putting their own selfish power ahead of everything else. He sees this as pointless, as power for power's sake has no purpose.</p>
          </li>
          <li>
            <h4>The Republic</h4>
            <p>Vanzer sees the Republic as responsible for the destruction of his homeworld along with the Jedi Order and the Separatists, seeing them as uncaring. They used entire planetary systems as pawns in a galactic conflict and so there's no love lost from the end of the Republic.</p>
          </li>
          <li>
            <h4>The Empire</h4>
            <p>In the Empire Vanzer sees the worst parts of the former Republic magnified; a brutal militaristic conquest of the galaxy causing mass-upheaval and countless deaths. There also isn't even the pretense of democracy or concern for the citizens of the galaxy. Vanzer can keep his displeasure quiet in order to get by in the galaxy but he's not at all pleased with the current state of affairs.</p>
          </li>
          <li>
            <h4>The Sathari</h4>
            <p>Vanzer feels very protective of the Sathari, continuing his peace-keeper mindset from his time on Arbooine. He was part of a group of relatively few Sathari who fled Arbooine and has grave concern for the future of his species.</p>
          </li>
          <li>
            <h4>R</h4>
            <p>Vanzer doesn't quite know what to make of R yet. They've gotten along well in their communications so far but R has kept is cards very close to his chest, and this has not gone unnoticed. Vanzer's willing to go along with R's request for help for now, but expects answers soon.</p>
