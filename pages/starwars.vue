      <h2>Backstory</h2>
      <figure class="figure-right">
        <a href="~/static/images/starwars/Quolas-Elrit-Tree.png" target="_blank"><img src="~/static/images/starwars/Quolas-Elrit-Tree.png" class="img" width="333.33" height="211" alt="Elrit Tree"></a>
