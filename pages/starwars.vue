      <p>Vanzer Madaar was part of a group of Sathari peace-keepers on his homeworld of Arbooine in the Outer Rim. He lived in the great tree-city of Quolas, built into the boughs of a massive elrit tree. There was much feuding between the powerful Helshar and Tumris clans. Vanzer's group of peace-keepers would attempt to prevent disagreements between members of these clans from escalating into bloodshed.</p>
      <p>Vanzer originally had positive feelings toward the Jedi order, and associated with their tenets of peace-keeping, though by the time Vanzer learned of his force powers he was already too old to be considered to join the order. Instead he became a peace-keeper, and tried to develop his force powers alone. He saught to follow the approach the Jedi took toward problem-solving, preferring to engage with words, considering violence as a last resort. Through hearing stories of the Jedi and the Sith he became keenly aware of the pull of the dark side. He considered the protection of others above the advancement of his own developing force powers and saw the dark side as a corruptive influence, predicting it would lead to animosity and hatred, and so he never persued it. He likened it to the worst of the behaviours he observed between the Helshar and Tumris clans, and saw only pointless destruction if he went down that path.</p>
      <p>The Jedi came to Arbooine during the clone wars, seeking an alliance. Arbooine had remained neutral in their conflict with the separatists, wanting to keep out of the war. After the Jedi arrived, a separatist fleet thought Arbooine was going to side with the Jedi, and began a bombardment of the planet, causing innumerable casualties. Vanzer was part of a group that managed to secure a ship and rapidly fled, and later began a search for a new home.</p>
      <p>Due to the Jedi bringing war to his world, as well as the militarisation of the Jedi not fitting their role as peace-keepers, Vanzer became disillusioned with the order, feeling they had lost their way.</p>
      <p>Today he tries to maintain a low profile, keeping front-of-mind the knowledge that his force powers would attract the attention of the new Galactic Empire.</p>
