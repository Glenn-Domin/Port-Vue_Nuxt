      <h3>Background</h3>
      <figure class="figure-right">
        <img src="~/static/images/starwars/Pillar_of_the_Ancestors_CotG.png" class="img" width="381" height="487.5" alt="Pillar of the Ancestors" loading="lazy">
        <figcaption>Pillar of the Ancestors</figcaption>
      </figure>
      <p>Arbooine is covered almost entirely in dense and impenetrable temperate, ancient-growth forests and topped with small icecaps at both poles. It has no true oceans to speak of, but it has countless freshwater rivers and lakes, varying in size from small pools to inland seas. the planet is home to many native species, including harmless herbivores, such as the utarvix, and dangerous predators, like the venomous bearsloth.</p>

      <p>Out of all of Arbooine’s native life, only a singular sentient species called it home. The Sathari are a humanoid species of avian descent who have lived on Arbooine for countless eons. The Sathari are an insular people who declined to take any noticeable role in galactic politics.</p>

      <p>Arbooine’s sentient population was relatively small; Republic estimates placed it at roughly 70 million, sparsely distributed. The planet’s largest and most dense settlement by far was its capitol city of Zeilo, also home to Arbooine’s only spaceport. Other settlements are scattered across the planet’s surface, many established in the vast elrit trees. Still others range from villages of wooden houses to domes of metal and stone to nomadic tent-camps.</p>

      <p>The name “Arbooine” was given to the planet by an uninspired Republic cartographer when the Sathari name for the world proved unpronounceable in Basic (and without a beak). The planet came to the attention of the Republic High Command during the Clone Wars because of its proximity to the Diophos Spur, a little-used route into Separatist space. The Grand Army of the Republic planned to use Arbooine as a staging post for a surprise assault into Separatist territory, but the Separatists discovered the plot and bombarded the planet. Most inhabitants died, while a minority managed to flee the planet.</p>

      <p>Since that time, what's left of Arbooine has once again faded from the galactic stage. Although Arbooine is part of the Empire, Imperial forces are seldom seen there. However, within the last year, Imperial Command has determined that Arbooine’s remoteness and countless kilometers of dense forest make it a possible location for a hidden Rebel base. In response to the recent Rebel attacks in the region, Imperial forces have been dispatched to search and secure the planet as needed.</p>
